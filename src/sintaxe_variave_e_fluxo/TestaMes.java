package sintaxe_variave_e_fluxo;

public class TestaMes {
	public static void main(String[] args) {
		int mes = 10;

		switch (mes) {
		case 1:
			System.out.println("Esse é o mês de janeiro");
			break;
		case 2:
			System.out.println("Esse é mês de fevreiro");
			break;
		case 3:
			System.out.println("Esse é mês de março");
			break;
		case 4:
			System.out.println("Esse é mês de abril");
			break;
		case 5:
			System.out.println("Esse é mês de maio");
			break;
		case 6:
			System.out.println("Esse é mês de junho");
			break;
		case 7:
			System.out.println("Esse é mês de julho");
			break;
		case 8:
			System.out.println("Esse é mês de agosto");
			break;
		case 9:
			System.out.println("Esse é mês de setembro");
			break;
		case 10:
			System.out.println("Esse é mês de outubro");
			break;
		case 11:
			System.out.println("Esse é mês de novembro");
			break;
		case 12:
			System.out.println("Esse é mês de Dezembro");
			break;
		default:
			System.out.println("mês inválido");
		}
	}
}
